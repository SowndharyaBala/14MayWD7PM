package com.operations;

public class Assignment_Operator {

    public static void main(String[] args){

        int num = 56;
        System.out.println(num);

       // num = num+5;
        num+=5;
        System.out.println(num);

        num-=10;   //num = num-10           // num = 61-10        //num = 51
        System.out.println(num);

        num*= 10;
        System.out.println(num);

        num/=10;
        System.out.println(num);

        num%=2;
        System.out.println(num);
    }
}
