package com.casting;

public class Implicit_Casting {
    public static void main(String[] args) {

        byte b = 56;
        long l = b;
        System.out.println(b);
        System.out.println(l);

        byte b1 = 56;
        double d = b1;
        System.out.println(b1);
        System.out.println(d);

    }
}
